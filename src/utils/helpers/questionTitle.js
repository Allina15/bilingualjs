import { QUESTION_TITLES } from '../constants'

export const questionTitle = (name) => {
   switch (name) {
      case QUESTION_TITLES.SELECT_REAL_ENGLISH_WORDS:
         return QUESTION_TITLES.SELECT_REAL_ENGLISH_WORDS

      case QUESTION_TITLES.LISTEN_AND_SELECT_WORD:
         return QUESTION_TITLES.LISTEN_AND_SELECT_WORD

      case QUESTION_TITLES.TYPE_WHAT_YOU_HEAR:
         return QUESTION_TITLES.TYPE_WHAT_YOU_HEAR

      case QUESTION_TITLES.DESCRIBE_IMAGE:
         return QUESTION_TITLES.DESCRIBE_IMAGE

      case QUESTION_TITLES.RESPOND_IN_AT_LEAST_N_WORDS:
         return QUESTION_TITLES.RESPOND_IN_AT_LEAST_N_WORDS

      case QUESTION_TITLES.RECORD_SAYING:
         return QUESTION_TITLES.RECORD_SAYING

      case QUESTION_TITLES.HIGHLIGHTS_THE_ANSWER:
         return QUESTION_TITLES.HIGHLIGHTS_THE_ANSWER

      case QUESTION_TITLES.SELECT_MAIN_IDEA:
         return QUESTION_TITLES.SELECT_MAIN_IDEA

      case QUESTION_TITLES.SELECT_THE_BEST_TITLE:
         return QUESTION_TITLES.SELECT_THE_BEST_TITLE
      default:
         return name
   }
}
